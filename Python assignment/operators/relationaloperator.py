#to print relational operators

def relational_operations(a, b):
    print(a < b)     #----------> lessthan
    print(a > b)     #----------> greaterthan
    print(a <= b)    #----------> lessthan equals to 
    print(a >= b)    #----------> greaterthan equals to 
    print(a == b)    #----------> equals to
relational_operations(5, 10)